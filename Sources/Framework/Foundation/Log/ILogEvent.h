#pragma once
#include "LogType.h"

namespace ob::foundation {

    //@―---------------------------------------------------------------------------
    //! @brief      ログ・イベント・インターフェース
    //! 
    //! @details    Logger に登録することでログの通知を受け取れるようになります。
    //@―---------------------------------------------------------------------------
    class ILogEvent {
    public:

        //@―---------------------------------------------------------------------------
        //! @brief      デストラクタ
        //@―---------------------------------------------------------------------------
        virtual ~ILogEvent() = default;


        //@―---------------------------------------------------------------------------
        //! @brief      Logger にイベントを登録したときに呼び出される
        //@―---------------------------------------------------------------------------
        virtual void onRegistered() {};


        //@―---------------------------------------------------------------------------
        //! @brief      Logger からイベントが登録解除されたときに呼び出される
        //@―---------------------------------------------------------------------------
        virtual void onUnregistered() {};


        //@―---------------------------------------------------------------------------
        //! @brief      ログ受信イベント
        //! 
        //! @param log  ログ情報
        //@―---------------------------------------------------------------------------
        virtual void onReceived(const Log& log) {};


        //@―---------------------------------------------------------------------------
        //! @brief      Loggerの終了通知
        //! 
        //! @details    プログラムの終了に呼び出される。
        //@―---------------------------------------------------------------------------
        virtual void onClosed() {};

    };

}// namespcae ob