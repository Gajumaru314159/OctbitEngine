#pragma once
#include <Framework/Core/Job/JobSystem.h>
#include <Framework/Core/Job/JobExecutor.h>
#include <Framework/Core/Job/JobGroup.h>

namespace ob {

	//@―---------------------------------------------------------------------------
	//! @brief コンストラクタ
	//@―---------------------------------------------------------------------------
	JobSystem::JobSystem() {
		
	}

	//@―---------------------------------------------------------------------------
	//! @brief ルートJobGroupを取得
	//@―---------------------------------------------------------------------------
	auto JobSystem::getRootGroup() -> JobGroup& {
		return *m_rootGroup;
	}

	//@―---------------------------------------------------------------------------
	//! @brief 実行
	//@―---------------------------------------------------------------------------
	void JobSystem::execute() {
		m_executor->execute();
	}

	//@―---------------------------------------------------------------------------
	//! @brief 待機
	//@―---------------------------------------------------------------------------
	void JobSystem::wait() {
		m_executor->wait();
	}

}