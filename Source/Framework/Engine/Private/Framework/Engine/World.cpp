#pragma once
#include <Framework/Engine/World.h>

namespace ob::engine {

	//@―---------------------------------------------------------------------------
	//! @brief		コンストラクタ
	//@―---------------------------------------------------------------------------
	World::World(StringView name) 
		: m_name(name)
	{
		createRootScene();
	}
	//@―---------------------------------------------------------------------------
	//! @brief		コンストラクタ
	//@―---------------------------------------------------------------------------
	World::~World() {

	}

	//@―---------------------------------------------------------------------------
	//! @brief		ルートシーン取得
	//@―---------------------------------------------------------------------------
	const Ref<Scene>& World::getRootScene()const {
		return m_scene;
	}


	//@―---------------------------------------------------------------------------
	//! @brief		ルートシーン生成
	//@―---------------------------------------------------------------------------
	void World::createRootScene() {
		if (m_scene)return;

		m_scene = Scene::Create(Format(TC("Root({})"),m_name));

	}

}// namespcae ob
