namespace ob::rhi::dx12 {
    class DeviceImpl;
    class ITexture;
}


//===============================================================
// クラス宣言
//===============================================================
namespace ob::rhi::dx12 {

    //@―---------------------------------------------------------------------------
    //! @brief  説明
    //@―---------------------------------------------------------------------------
    class DisplayImpl :public rhi::IDisplay {
    public:

        //===============================================================
        // コンストラクタ / デストラクタ
        //===============================================================

        //@―---------------------------------------------------------------------------
        //! @brief  コンストラクタ
        //@―---------------------------------------------------------------------------
        DisplayImpl(DeviceImpl& rDevice, const DisplayDesc& desc);


        //@―---------------------------------------------------------------------------
        //! @brief  デストラクタ
        //@―---------------------------------------------------------------------------
        ~DisplayImpl();


        //@―---------------------------------------------------------------------------
        //! @brief  妥当なオブジェクトか
        //@―---------------------------------------------------------------------------
        bool isValid()const override;


        //===============================================================
        // ゲッター
        //===============================================================

        //@―---------------------------------------------------------------------------
        //! @brief  バックバッファの数を取得
        //@―---------------------------------------------------------------------------
        const DisplayDesc& getDesc()const noexcept override;


        //===============================================================
        // 更新
        //===============================================================

        //@―---------------------------------------------------------------------------
        //! @brief  バックバッファのサイズを変更
        //@―---------------------------------------------------------------------------
        bool resizeBackBuffer(const Size& size) override;


        //@―---------------------------------------------------------------------------
        //! @brief      更新
        //! 
        //! @details    表示するテクスチャを次のバックバッファにします。
        //@―---------------------------------------------------------------------------
        void update() override;


        //@―---------------------------------------------------------------------------
        //! @brief      デスクリプタCPUハンドルを取得
        //@―---------------------------------------------------------------------------
        D3D12_CPU_DESCRIPTOR_HANDLE getCpuHandle()const;


        //@―---------------------------------------------------------------------------
        //! @brief      デスクリプタGPUハンドルを取得
        //@―---------------------------------------------------------------------------
        D3D12_GPU_DESCRIPTOR_HANDLE getGpuHandle()const;


        //@―---------------------------------------------------------------------------
        //! @brief      ビューポートを取得
        //@―---------------------------------------------------------------------------
        D3D12_VIEWPORT getViewport()const;


        //@―---------------------------------------------------------------------------
        //! @brief      シザー矩形を取得
        //@―---------------------------------------------------------------------------
        D3D12_RECT getScissorRect()const;


        //@―---------------------------------------------------------------------------
        //! @brief      リソース取得
        //@―---------------------------------------------------------------------------
        ID3D12Resource* getResource()const;

    private:

        //@―---------------------------------------------------------------------------
        //! @brief      レンダーテクスチャを初期化
        //@―---------------------------------------------------------------------------
        bool createDisplay(DeviceImpl& rDevice);


        //@―---------------------------------------------------------------------------
        //! @brief      レンダーテクスチャを初期化
        //@―---------------------------------------------------------------------------
        bool createBuffer(DeviceImpl& rDevice);


        //@―---------------------------------------------------------------------------
        //! @brief      カラースペースを設定
        //@―---------------------------------------------------------------------------
        bool setColorSpace();


        //@―---------------------------------------------------------------------------
        //! @brief      名前変更時
        //@―---------------------------------------------------------------------------
        void onNameChanged()override;


    private:

        DisplayDesc m_desc;

        ComPtr<IDXGISwapChain4> m_swapChain;            //!< スワップチェイン
        Array<ComPtr<ID3D12Resource>> m_buffers;       //!< バックバッファ

        D3D12_VIEWPORT m_viewport;                      //!< ビューポート
        D3D12_RECT m_scissorRect;                       //!< シザー矩形

        DescriptorHandle m_hRTV;
        s32 m_frameIndex;

        UINT m_syncInterval;

        bool m_initialized = false;



    };

}// namespcae ob::rhi::dx12