#pragma once
#include <Framework/Core/CoreTypes.h>

namespace ob::core {

	//@―---------------------------------------------------------------------------
	//! @brief  ポイント
	//@―---------------------------------------------------------------------------
	struct OB_API Point {
	public:

		//@―---------------------------------------------------------------------------
		//! @brief コンストラクタ(初期化なし)
		//@―---------------------------------------------------------------------------
		Point() = default;


		//@―---------------------------------------------------------------------------
		//! @brief コンストラクタ(値で初期化)
		//@―---------------------------------------------------------------------------
		constexpr Point(s32 x, s32 y, s32 z = 0) noexcept
			: x(x), y(y), z(z) {}


		//@―---------------------------------------------------------------------------
		//! @brief 設定
		//@―---------------------------------------------------------------------------
		constexpr void set(s32 x, s32 y, s32 z = 0) noexcept {
			this->x = x;
			this->y = y;
			this->z = z;
		}

	public:

		s32 x=0;  //!< X座標
		s32 y=0;  //!< Y座標
		s32 z=0;  //!< Z座標

	};

}// namespcae ob